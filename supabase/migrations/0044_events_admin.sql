-- プルミエ！ 0044 — イベントのサムネイルを運営が管理できるようにする
--   events は 0007 で select(true) のみ・書き込みポリシー無し（＝一般ユーザーは
--   更新不可）だった。運営専用のサムネイル管理画面から image_url を差し替えられる
--   よう、is_admin() に限定した update ポリシーを追加する。
--   ・掲載できるのは主催から許諾を得た画像／運営が自ら撮影した会場写真のみ
--     （プレス審査制の公式ロゴ・キービジュアルは対象外。チェックリスト参照）。
--   ・行の新規作成・削除は引き続き SQL Editor（サービスロール）のみ。ここで
--     開けるのは update だけなので、運営が誤ってイベントを消すことはできない。
-- =============================================================================

-- is_admin() は 0023 で定義済み（SECURITY DEFINER）。
create policy events_admin_update on events
  for update to authenticated
  using (is_admin())
  with check (is_admin());
