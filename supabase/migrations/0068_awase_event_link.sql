-- =============================================================================
-- プルミエ！ 0068 — 併せ（awase）とイベントの構造的な紐付け
--   「行ってみたい」で興味を持ったイベントから、そのまま「この日に一緒に回る
--   仲間を探す/募集する」導線を作るための土台。awase に event_id（任意）を
--   持たせ、イベント詳細で「このイベントの併せ」を正確に一覧できるようにする。
--   Twitter のハッシュタグ検索では作れない、イベント×併せの構造的な結び付き。
--
--   既存の併せは event_id = NULL のまま（イベント非依存の併せも従来どおり可）。
--   イベント削除時は set null（併せ自体は残す。日程・地域は awase 側に保持済み）。
-- =============================================================================

alter table awase
  add column event_id uuid references events (id) on delete set null;

-- イベント詳細で「このイベントの公開中の併せ」を引くための部分インデックス。
create index awase_event_idx on awase (event_id) where status = 'open';
