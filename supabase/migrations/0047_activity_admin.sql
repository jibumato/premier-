--   activity_events（0046）は誰でも閲覧可・書き込みはトリガー（SECURITY DEFINER）
--   経由のみで、削除ポリシーが一切無かった。誤った/不適切な見出しの個別削除や、
--   増え続ける行の定期整理を運営画面から行えるよう、is_admin() に限定した
--   delete ポリシーを追加する。insert/update は引き続き用意しない
--   （内容はトリガー生成のみ・運営でも書き換え不可）。
-- =============================================================================

create policy activity_events_admin_delete on activity_events
  for delete to authenticated
  using (is_admin());
