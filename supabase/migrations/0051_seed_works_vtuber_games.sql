-- =============================================================================
-- プルミエ！ 0051 — VTuber・オンラインゲーム界隈の作品追加＋「その他」
--   0016 でホロライブ/にじさんじは追加済みだが、VTuberグループの一角である
--   「ぶいすぽっ!」が未収録だった。また、コスプレ需要の高い対戦ゲーム
--   （FPS/MOBA）が works に無かったため、代表的なタイトルを追加する。
--   さらに、カタログに無い作品向けの受け皿として「その他」を追加する
--   （既存の「オリジナル・創作」は完全オリジナル衣装向けの選択肢で趣旨が
--   異なるため、既存作品だが未収録の場合の選択肢として別に用意する）。
--   冪等（on conflict do nothing、再実行安全）。reading は 0040 と同じ方針で
--   あいうえお順ソート用のかなを付与する。
-- =============================================================================

insert into works (name) values
  -- VTuber
  ('ぶいすぽっ!'),
  -- オンラインゲーム（対戦・FPS/MOBA。コスプレイベントでの露出が高い定番タイトル）
  ('Apex Legends'),
  ('League of Legends'),
  ('オーバーウォッチ'),
  ('VALORANT'),
  ('フォートナイト'),
  -- カタログに見当たらない既存作品向けの受け皿
  ('その他')
on conflict (name) do nothing;

update works as w set reading = v.reading
from (values
  ('ぶいすぽっ!', 'ぶいすぽ'),
  ('Apex Legends', 'えーぺっくすれじぇんず'),
  ('League of Legends', 'りーぐおぶれじぇんど'),
  ('オーバーウォッチ', 'おーばーうぉっち'),
  ('VALORANT', 'ゔぁろらんと'),
  ('フォートナイト', 'ふぉーとないと'),
  ('その他', 'そのた')
) as v(name, reading)
where w.name = v.name;
