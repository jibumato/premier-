-- プルミエ！ 0054 — @ユーザーネーム（人物検索の土台）＋表示名検索の許可フラグ
--   人物検索を「知っている人を探す」体験に限定するための下ごしらえ。
--   ・handle 列は 0001 で既に存在（unique・^[a-z0-9_]{3,20}$）。ユーザーが
--     プロフィール編集から自分の @ユーザーネーム を設定できるようにする
--     （更新は既存の profiles_update RLS: id=auth.uid() でそのまま本人限定）。
--     登録時は user_xxxxxxxx が自動採番されるため、未設定＝自動値のまま。
--   ・searchable_by_name: 表示名（重複可）での検索を許可するか。既定 false。
--     つきまとい対策として、表示名での総当たり検索は本人がオンにした人だけ
--     ヒットするようにする（@ユーザーネーム検索は常に可・非公開は除外）。
--   冪等（add column if not exists、再実行安全）。
-- =============================================================================

alter table profiles add column if not exists searchable_by_name boolean not null default false;
